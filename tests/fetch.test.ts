import { fetchUserData } from "../src/fetchUserdata";
import { describe, expect, test } from "vitest";
type User = {
  id: number;
  name: string;
  username: string;
  email: string;
  address: {
    street: string;
    suite: string;
    city: string;
    zipcode: string;
    geo: {
      lat: string;
      lng: string;
    };
  };
  phone: string;
  website: string;
  company: {
    name: string;
    catchPhrase: string;
    bs: string;
  };
};

describe("get user by id", () => {
  test("getting user by correct id", async () => {
    const expected: User = {
      id: 2,
      name: "Ervin Howell",
      username: "Antonette",
      email: "Shanna@melissa.tv",
      address: {
        street: "Victor Plains",
        suite: "Suite 879",
        city: "Wisokyburgh",
        zipcode: "90566-7771",
        geo: {
          lat: "-43.9509",
          lng: "-34.4618",
        },
      },
      phone: "010-692-6593 x09125",
      website: "anastasia.net",
      company: {
        name: "Deckow-Crist",
        catchPhrase: "Proactive didactic contingency",
        bs: "synergize scalable supply-chains",
      },
    };
    const id = 2;
    const user = await fetchUserData(id);
    expect(user).toEqual(expected);
  });

  test("getting error while incorrect id", async () => {
    await expect(fetchUserData(8658)).rejects.toThrowError();
  });
});
